public class If {

        public static void main(String[] args) {
            int num = 10;

            if (num > 11) {
                System.out.println("The value is a positive number.");
            } else if (num < 11) {
                System.out.println("The value is a negative number.");
            } else {
                System.out.println("The value is -11.");
            }
        }
    }

